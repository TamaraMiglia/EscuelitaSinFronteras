    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String ValNom = txtNombre.getText();
        String ValAp = txtApellido.getText();
        String ValDni = txtDni.getText();
        String ValCar = txtCarrera.getText();
        
        if (!ValNom.equals(""))
        {if (!ValAp.equals(""))
        {if (!ValDni.equals(""))
        { if (!ValCar.equals(""))
        {
            Alumnos al = new Alumnos(ValNom, ValAp, Integer.parseInt(ValDni), ValCar);
            Agenda.add(al);
            
            JOptionPane.showMessageDialog(null, "Alumno guardado con exito");
            txtNombre.setText("");
            txtApellido.setText("");
            txtDni.setText("");
            txtCarrera.setText("");
            
            } else 
        {
            JOptionPane.showMessageDialog(null,"El campo Carrera esta vacio");
        }} else 
        {
            JOptionPane.showMessageDialog(null,"El campo Dni esta vacio");
        }} else 
        {
            JOptionPane.showMessageDialog(null,"El campo Apellido esta vacio");
        }
        } else 
        {
            JOptionPane.showMessageDialog(null,"El campo Nombre esta vacio");
        }
        
        Object o [] = null;
        for (int i = 0; i < Agenda.size(); i++) {
           Alumnos c = (Alumnos) Agenda.get(i);
            t.addRow(o);
            t.setValueAt(c.getNombre(), i, 1);
            t.setValueAt(c.getApellido(), i, 2);
            t.setValueAt(c.getDni(), i, 0);
            t.setValueAt(c.getCarrera(), i, 3);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

